class TestActionAccumulator {
    /**
     * Fill the accumulator with numbers from 1 to buckets * capacity.
     * @param acc
     */
    private fun fill(acc: ActionAccumulator?) {
        val buckets = acc.numberOfBuckets()
        val capacity = acc.bucketCapacity()
        // Add different amounts to better check consistency.
        for (i in 1..buckets * capacity) {
            acc.add(i.toFloat())
        }
    }

    /**
     * Check if values and counts are as if fill was called on empty acc.
     * @param acc
     */
    private fun checkIncreasingValues(acc: ActionAccumulator?) {
        val buckets = acc.numberOfBuckets()
        val capacity = acc.bucketCapacity()
        for (i in 0 until buckets) {
            if (acc.bucketCount(i) != capacity) Assert.fail("Bad capacity at " + i + ": " + acc.bucketCount(i) + " / " + capacity)
            val start = (buckets - 1 - i) * capacity + 1
            val end = start + capacity - 1
            val expect = end * (end + 1) / 2 - (start - 1) * (start - 1 + 1) / 2
            if (acc.bucketScore(i) != expect.toFloat()) Assert.fail(
                "Bad value at bucket " + i + ": " + acc.bucketScore(
                    i
                ) + " / " + expect
            )
        }
        if (acc.count() != buckets * capacity) Assert.fail("Total count.")
        if (acc.score() != (buckets * capacity * (buckets * capacity + 1) / 2).toFloat()) Assert.fail("Total score.")
    }

    @Test
    fun testFill() {
        val acc = ActionAccumulator(50, 10)
        fill(acc)
        checkIncreasingValues(acc)
    }

    @Test
    fun testClear() {
        val acc = ActionAccumulator(50, 10)
        fill(acc)
        acc.clear()
        if (acc.count() != 0) Assert.fail("Expect 0 count after clear, got: " + acc.count())
        if (acc.score() != 0f) Assert.fail("Expect 0 score after clear, got: " + acc.score())
        for (i in 0 until acc.numberOfBuckets()) {
            if (acc.bucketCount(i) != 0) Assert.fail(
                "Expect 0 count at " + i + " after clear, got: " + acc.bucketCount(
                    i
                )
            )
            if (acc.bucketScore(i) != 0f) Assert.fail(
                "Expect 0 score at " + i + " after clear, got: " + acc.bucketScore(
                    i
                )
            )
        }
    }
}